function renderResult() {
  var a = getApp();
  if (!a) return;
  setMode('result');
  animatePage();
  var s = loadState();
  var total = currentQList.length;
  var score = s.score || 0;
  var pct = Math.round((score / total) * 100);
  var stars = pct >= 80 ? '⭐⭐⭐' : pct >= 60 ? '⭐⭐' : '⭐';
  var msg = pct >= 80 ? '🎉 عالی! آفرین!' : pct >= 60 ? '👍 خوب بود، ادامه بده!' : '📖 نیاز به مطالعه بیشتر داری.';

  a.innerHTML = '<div class="header">🧠 نتیجه آزمون</div>'
    + '<div class="result-screen page-enter">'
    + '<div style="font-size:2rem">'+stars+'</div>'
    + '<div style="margin-top:8px;font-size:1.1rem">'+msg+'</div>'
    + '<div class="score">'+score+' / '+total+'</div>'
    + '<div class="score-detail">درصد صحیح: '+pct+'٪</div>'
    + '</div>'
    + '<div class="action-row">'
    + '<button class="btn" onclick="startMode(\''+s.catId+'\')">🔄 تکرار</button>'
    + '<button class="btn dark" onclick="renderHome()">🏠 خانه</button>'
    + '</div>';

  s.qIndex = 0; s.score = 0; s.answered = false; s.mode = 'result';
  saveState(s);
}

