function renderHome() {
  var a = getApp();
  if (!a) { console.error('appContainer not found!'); return; }
  setMode('home');
  animatePage();

  var s = loadState();
  var res = s.results || {};

  var savedHtml = '';
  if (s.mode === 'quiz' && typeof s.qIndex === 'number' && s.qIndex > 0) {
    var catLabel = s.catId === 'all' ? 'همه سوالات'
      : s.catId === 'pre' ? 'قبل از ۱۴۰۰'
      : s.catId === 'post' ? '۱۴۰۰ به بعد'
      : (CATEGORIES.find(function(c){return c.id===s.catId;}) || {name:'...'}).name;
    savedHtml = '<div class="saved-badge">📌 ادامه از: <b>'+catLabel+'</b> — سوال '+(s.qIndex+1)+'</div>'
      + '<button class="btn" onclick="continueSaved()">← ادامه جلسه قبل</button>';
  }

  var catCards = CATEGORIES.map(function(cat) {
    var catQs = questions.filter(function(q){return q.cat===cat.id;});
    var done = catQs.filter(function(_,i){return res[cat.id+'_'+i] !== undefined;}).length;
    var correct = catQs.filter(function(_,i){return res[cat.id+'_'+i] === true;}).length;
    var prog = done > 0 ? '✅ '+correct+'/'+done : '';
    return '<div class="cat-card" onclick="startMode(\''+cat.id+'\')">'
      +'<div class="cat-icon">'+cat.icon+'</div>'
      +'<div class="cat-name">'+cat.name+'</div>'
      +'<div class="cat-count">'+catQs.length+' سوال</div>'
      +'<div class="cat-progress">'+prog+'</div>'
      +'</div>';
  }).join('');

  var totalDone = Object.keys(res).length;
  var totalCorrect = Object.values(res).filter(function(v){return v===true;}).length;
  var totalPct = totalDone > 0 ? Math.round((totalCorrect/totalDone)*100) : 0;

  a.innerHTML = '<div class="header">🧠 کنکور ارشد روانشناسی</div>'
    + savedHtml
    + '<div class="mini-stats">'
    + '<div class="mini-stat"><div class="val">'+questions.length+'</div><div class="lbl">کل سوال</div></div>'
    + '<div class="mini-stat"><div class="val">'+totalDone+'</div><div class="lbl">پاسخ داده</div></div>'
    + '<div class="mini-stat"><div class="val">'+totalCorrect+'</div><div class="lbl">صحیح</div></div>'
    + '<div class="mini-stat"><div class="val">'+totalPct+'٪</div><div class="lbl">درصد</div></div>'
    + '</div>'
    + '<div class="cat-card all-mode" onclick="startMode(\'all\')">'
    + '<div class="cat-icon">📋</div>'
    + '<div class="cat-name">همه سوالات ('+questions.length+' سوال)</div>'
    + '</div>'
    + '<div class="cat-grid">'+catCards+'</div>'
    + '<div style="display:flex;gap:10px">'
    + '<button class="btn warn" onclick="startMode(\'pre\')">📜 کلاسیک ('+questions.filter(function(q){return q.era==='pre';}).length+')</button>'
    + '<button class="btn info" onclick="startMode(\'post\')">🆕 ۱۴۰۰+ ('+questions.filter(function(q){return q.era==='post';}).length+')</button>'
    + '</div>'
    + '<hr class="divider">'
    + '<div style="display:flex;gap:10px">'
    + '<button class="btn secondary" onclick="renderAbout()">ℹ️ درباره اپ</button>'
    + '<button class="btn dark" onclick="confirmReset()">🗑 پاک کردن پیشرفت</button>'
    + '</div>';
}

function continueSaved() {
  var s = loadState();
  if (!s.catId) return;
  buildQList(s.catId);
  state = s;
  renderQuestion();
}

function buildQList(catId) {
  if (catId === 'all')       currentQList = questions.slice();
  else if (catId === 'pre')  currentQList = questions.filter(function(q){return q.era==='pre';});
  else if (catId === 'post') currentQList = questions.filter(function(q){return q.era==='post';});
  else                       currentQList = questions.filter(function(q){return q.cat===catId;});
}

function startMode(catId) {
  buildQList(catId);
  var s = loadState();
  state = { mode:'quiz', catId:catId, qIndex:0, score:0, answered:false, results: s.results || {} };
  saveState(state);
  renderQuestion();
}

function confirmReset() {
  if (confirm('آیا مطمئن هستید؟\nتمام پیشرفت پاک می‌شود.')) {
    try { localStorage.removeItem(STORAGE_KEY); } catch(e) {}
    _memStore = {};
    state = {};
    renderHome();
  }
}

