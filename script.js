var currentQList = [];
var state = {};
var app = null;

function getApp() {
  if (!app) app = document.getElementById("appContainer");
  return app;
}

function animatePage() {
  var a = getApp();
  if (!a) return;
  a.classList.remove('page-enter');
  void a.offsetWidth;
  a.classList.add('page-enter');
}

function setMode(mode) {
  window.__currentMode = mode;
  try { history.pushState({ page: mode }, ''); } catch(e) {}
}

