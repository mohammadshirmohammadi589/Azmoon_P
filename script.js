function renderQuestion() {
  var a = getApp();
  if (!a) return;
  setMode('quiz');
  animatePage();
  state = loadState();

  var qi = state.qIndex || 0;
  if (qi >= currentQList.length) { renderResult(); return; }

  var q = currentQList[qi];
  var total = currentQList.length;
  var pct = Math.round((qi / total) * 100);
  var catInfo = CATEGORIES.find(function(c){return c.id===q.cat;}) || {name:'', icon:''};
  var eraBadge = q.era === 'pre'
    ? '<span class="era-badge era-before">📜 کلاسیک</span>'
    : '<span class="era-badge era-after">🆕 ۱۴۰۰+</span>';

  var optHtml = q.o.map(function(opt, i) {
    return '<button class="option-btn" id="opt'+i+'" onclick="answer('+i+')">'+letters_arr[i]+'. '+opt+'</button>';
  }).join('');

  a.innerHTML = '<div class="header">🧠 روانشناسی کنکور ارشد</div>'
    + '<div class="progress-text">سوال '+(qi+1)+' از '+total+' &nbsp;|&nbsp; امتیاز: '+(state.score||0)+'</div>'
    + '<div class="progress-bar-wrap"><div class="progress-bar-fill" style="width:'+pct+'%"></div></div>'
    + '<div class="question-box">'
    + '<div style="margin-bottom:6px">'+eraBadge+'<span class="category-badge">'+catInfo.icon+' '+catInfo.name+'</span></div>'
    + q.q
    + '</div>'
    + '<div class="options">'+optHtml+'</div>'
    + '<div id="feedbackBox" class="feedback hidden"></div>'
    + '<div class="action-row hidden" id="actionRow">'
    + '<button class="btn" onclick="nextQ()">سوال بعدی ←</button>'
    + '<button class="btn secondary" onclick="renderHome()">🏠 خانه</button>'
    + '</div>';
}

function answer(idx) {
  var s = loadState();
  if (s.answered) return;

  var q = currentQList[s.qIndex || 0];
  var isCorrect = (idx === q.c);

  s.answered = true;
  if (isCorrect) s.score = (s.score || 0) + 1;
  var rKey = s.catId + '_' + s.qIndex;
  if (!s.results) s.results = {};
  s.results[rKey] = isCorrect;
  saveState(s);
  state = s;

  var btns = document.querySelectorAll('.option-btn');
  for (var i = 0; i < btns.length; i++) {
    btns[i].classList.add('disabled');
    if (i === q.c) btns[i].classList.add('correct');
    else if (i === idx && !isCorrect) btns[i].classList.add('incorrect');
  }

  var fb = document.getElementById('feedbackBox');
  if (fb) {
    fb.classList.remove('hidden');
    fb.innerHTML = isCorrect
      ? '✅ <b>درست!</b> 💡 ' + q.ex
      : '❌ <b>اشتباه!</b> پاسخ صحیح: <b>'+letters_arr[q.c]+'. '+q.o[q.c]+'</b><br>💡 '+q.ex;
  }
  var ar = document.getElementById('actionRow');
  if (ar) ar.classList.remove('hidden');
}

function nextQ() {
  var s = loadState();
  s.qIndex = (s.qIndex || 0) + 1;
  s.answered = false;
  saveState(s);
  state = s;
  if (s.qIndex >= currentQList.length) renderResult();
  else renderQuestion();
}

